를 통해 작성하는 것.
m 컴퓨터가 처리하기 때문에 프로그램은 컴퓨터가 이해할 수 있는 특정 언어로
작성된다.
m 컴퓨터 프로그램을 작성할 때 사용하는 언어를 ‘프로그래밍 언어’라 한다.
<프로그래밍 언어(Programming Language)>
ž 사람이 이해할 수 있는 형태로는 컴퓨터에게 일을 시킬 수 없기 때문에
컴퓨터가 이해할 수 있게 고안된 언어로서 현재 매우 많은 종류의 언어가
존재한다.
ex) C, C++, JAVA, Perl, Ruby, BASIC ...
그러나 이 언어들도 컴퓨터가 완벽히 이해할 수 있는 형태가 아니기 때문
에 중간에 기계어로 번역해 주어야 한다.

